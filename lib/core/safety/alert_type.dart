/// Alert type enumeration
enum AlertType {
  /// Critical health concerns requiring immediate attention
  safety,

  /// Health concerns requiring monitoring
  warning,

  /// Helpful health information
  information;
}

/// Alert severity enumeration
enum AlertSeverity {
  /// High severity - requires immediate attention
  high,

  /// Medium severity - requires monitoring
  medium,

  /// Low severity - informational
  low;
}

